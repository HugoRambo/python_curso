# Exercício Python 5: Faça um programa que leia um número Inteiro e mostre na tela o seu sucessor e seu antecessor.
a = int(input('Digite um numero que queria descobrir, o sucesso e antecessor: '))
s = a + 1
resultado = a - 1
print(f'O sucessor do {a} ', f'é o numero {s}', f'e o antecessor é {resultado}')