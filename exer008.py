# Exercício Python 8: Escreva um programa que leia um valor em metros e o exiba convertido em centímetros e milímetros.
valor = int(input('Digite um valor em metros, para convertermos em centimetros e milimetros:'))
print(f'Resultado de metro para centimetro {valor * 100}\n', f'Valor de metros para milimetro {valor * 1000}')
